import Image from "next/image";
import { useEffect, useState } from "react";

const ShimmerMessages = () => {
  const messages = [
    "Thinking...",
    "Loading...",
    "Generating...",
    "Analyzing your request...",
    "Building your website...",
    "Crafting components...",
    "Optimizing layout...",
    "Adding final touches...",
    "Almost ready...",
  ];

  const [currentMessagesIndex, setCurrentMessagesIndex] = useState(0);

  useEffect(() => {
    const interval = setInterval(() => {
      setCurrentMessagesIndex((prev) => (prev + 1) % messages.length);
    }, 5000);
    return () => clearInterval(interval);
  }, [messages.length]);

  return (
    <div className="flex items-center gap-2">
      <span className="text-base text-muted-foreground animate-pulse">
        {messages[currentMessagesIndex]}
      </span>
    </div>
  );
};

function MessageLoading() {
  return (
    <div className="flex flex-col group px-2 pb-4">
      <div className="flex items-center gap-2 pl-2 mb-2">
        <Image
          src={"/logo.svg"}
          alt="Promptly"
          height={18}
          width={18}
          className="shrink-0"
        ></Image>
        <span className="text-sm font-medium">Promptly</span>
      </div>
      <div className="pl-8.5 flex flex-col gap-y-4">
        <ShimmerMessages></ShimmerMessages>
      </div>
    </div>
  );
}
export default MessageLoading;